def generate_memory_tip(herb_name, effects):
    """生成记忆口诀"""
    # 优先使用经典口诀
    if herb_name in MEMORY_TIPS:
        return MEMORY_TIPS[herb_name]
    
    # 根据功效生成口诀
    if effects:
        # 提取关键词
        keywords = []
        if '解表' in effects:
            keywords.append('解表')
        if '清热' in effects:
            keywords.append('清热')
        if '活血' in effects:
            keywords.append('活血')
        if '补气' in effects:
            keywords.append('补气')
        if '补血' in effects:
            keywords.append('补血')
        if '利水' in effects or '渗湿' in effects:
            keywords.append('利水')
        if '祛风' in effects or '除湿' in effects:
            keywords.append('祛风')
        if '安神' in effects:
            keywords.append('安神')
        if '止咳' in effects or '化痰' in effects:
            keywords.append('止咳')
        if '平肝' in effects:
            keywords.append('平肝')
        
        if keywords:
            return f"{herb_name}{'、'.join(keywords[:2])}效果好"
    
    # 默认口诀
    return f"{herb_name}功效独特，临床应用记心间"

def main():
    with open('herbs_split.json', 'r', encoding='utf-8') as f:
        herbs = json.load(f)
    
    print(f"🌿 开始生成记忆口诀（共 {len(herbs)} 味药材）\n")
    
    updated = 0
    for herb in herbs:
        name = herb['name']
        effects = herb.get('effects', '')
        if isinstance(effects, list):
            effects = ' '.join(effects)
        
        # 生成新的memoryTip
        new_tip = generate_memory_tip(name, effects)
        herb['memoryTip'] = new_tip
        updated += 1
        
        if updated <= 20 or name in MEMORY_TIPS:
            print(f"✓ {name}: {new_tip}")
    
    # 保存
    with open('herbs_split.json', 'w', encoding='utf-8') as f:
        json.dump(herbs, f, ensure_ascii=False, indent=2)
    
    print(f"\n✅ 完成！已为 {updated} 味药材生成记忆口诀")

if __name__ == "__main__":
    main()
