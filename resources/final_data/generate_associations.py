def generate_association(herb_name, effects):
    """生成趣味联想"""
    # 优先使用已有联想
    if herb_name in ASSOCIATIONS:
        return ASSOCIATIONS[herb_name]
    
    # 根据功效生成
    if effects:
        if '解表' in effects:
            return f"{herb_name}像温暖的阳光，驱散风寒保健康"
        if '清热' in effects:
            return f"{herb_name}像清凉的泉水，清热泻火解暑热"
        if '活血' in effects:
            return f"{herb_name}像清道夫，活血化瘀通经络"
        if '补气' in effects:
            return f"{herb_name}像能量棒，补气提神精神爽"
        if '补血' in effects:
            return f"{herb_name}像红血球，补血养颜气色好"
        if '利水' in effects:
            return f"{herb_name}像排水管，利水消肿排废水"
        if '祛风' in effects:
            return f"{herb_name}像防风林，祛风除湿护关节"
        if '安神' in effects:
            return f"{herb_name}像安眠曲，宁心安神睡得香"
        if '止咳' in effects:
            return f"{herb_name}像止咳糖，咳嗽气喘都能止"
    
    # 默认
    return f"{herb_name}功效独特，就像专属的健康卫士"

def main():
    with open('herbs_split.json', 'r', encoding='utf-8') as f:
        herbs = json.load(f)
    
    print(f"🌿 开始生成趣味联想（共 {len(herbs)} 味药材）\n")
    
    updated = 0
    for herb in herbs:
        name = herb['name']
        effects = herb.get('effects', '')
        if isinstance(effects, list):
            effects = ' '.join(effects)
        
        # 生成新的association
        new_assoc = generate_association(name, effects)
        herb['association'] = new_assoc
        updated += 1
        
        if updated <= 20 or name in ASSOCIATIONS:
            print(f"✓ {name}: {new_assoc}")
    
    # 保存
    with open('herbs_split.json', 'w', encoding='utf-8') as f:
        json.dump(herbs, f, ensure_ascii=False, indent=2)
    
    print(f"\n✅ 完成！已为 {updated} 味药材生成趣味联想")

if __name__ == "__main__":
    main()
